/** Подбор флагмана по ключевым словам — чтобы «идеальный» отчёт показывался
 *  и на реальных идеях похожей темы, а не только на демо-id. */
const KEYWORD_FLAGSHIP: { id: string; words: string[] }[] = [
	{ id: "7", words: ["лендинг", "маркетплейс шаблон", "конструктор сайт"] },
	{ id: "8", words: ["дайджест", "рассылк", "newsletter", "саммари новост"] },
];

function pickFlagshipId(idea: Idea): string | null {
	if (RICH_REPORTS[idea.id]) return idea.id;
	const haystack = `${idea.title} ${idea.description}`.toLowerCase();
	for (const { id, words } of KEYWORD_FLAGSHIP) {
		if (words.some((w) => haystack.includes(w))) return id;
	}
	return null;
}

/** Генерирует «идеальный» отчёт для идеи. Флагманы — заданы вручную; для
 *  остальных строится структурно полный, но более осторожный отчёт. */
export function buildRichMockReport(
	idea: Idea,
	version = 1,
): RichAnalysisReport {
	const flagshipId = pickFlagshipId(idea);
	if (flagshipId) {
		return {
			...RICH_REPORTS[flagshipId],
			analyzedAt: new Date().toISOString(),
			version,
		};
	}
	return fallbackReport(idea, version);
}

